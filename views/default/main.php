<p class="date"> 18.05.1600</p>
<p class="news">Как заявил Председатель Комиссии ОП РФ по безопасности и взаимодействию с ОНК, в ближайшее время в регионах создадут рабочие группы по проверке автошкол.
Рабочие группы, состоящие из членов общественной палаты РФ, сотрудников Госавтоинспекции, прокуратуры и автошкол, начнут проверку готовности автошкол к обучению по новому регламенту МВД.
По итогам работы таких групп планируют выносить соответствующие управленческие решения для оптимизации процесса подготовки водителей.
